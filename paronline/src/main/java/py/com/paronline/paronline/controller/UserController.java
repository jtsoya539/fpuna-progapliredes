    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        UserService userService = new UserService();
        RequestDispatcher rd;

        String id = request.getParameter("id");

        if (id != null) {
            User usuario = userService.getUser(Integer.parseInt(id));
            request.setAttribute("user", usuario);

            rd = request.getRequestDispatcher("/view/user/details.jsp");
        } else {

            ArrayList<User> users = userService.getUsers();
            request.setAttribute("users", users);

            rd = request.getRequestDispatcher("/view/user/index.jsp");
        }

        rd.forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

    @Override
    protected void doDelete(HttpServletRequest req, HttpServletResponse resp) throws ServletException, IOException {
        String id = req.getParameter("id");

        if (id != null) {
            UserService userService = new UserService();
            userService.removeUser(Integer.parseInt(id));
        }

        RequestDispatcher rd = req.getRequestDispatcher("/view/user/index.jsp");
        rd.forward(req, resp);
    }

